public class CheckNum
{
    public static void main(String[] args)
    {
        int num = 37;
        System.out.println(num);
        if(num>0)
        {
            System.out.println("Positive Number");
        }
        else if(num<0)
        {
            System.out.println("Negative Number");
        }
        else
        {
            System.out.println("Zero");
        }
    }
}